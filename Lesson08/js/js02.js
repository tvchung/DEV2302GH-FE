// Toán tử
// Toán tử số học: *; / ; +; -; %; ++; --
var a = 5;
var b = 4;
console.log("a=",a,"; b=",b);
var result = a*b;
console.log(a,"*",b,"=",result);
result = a/b;
console.log(a,"/",b,"=",result);
result = a+b;
console.log(a,"+",b,"=",result);
result = a-b;
console.log(a,"-",b,"=",result);
result = a%b;
console.log(a,"%",b,"=",result);
// ++
res = a++; // sau câu lệnh thì giá trị của a mới tăng
console.log(a,"++ => ",res);
res = ++b; // tăng b lên 1 đơn vị sau đó mới gán cho res
console.log("++",b,"=>",res);

